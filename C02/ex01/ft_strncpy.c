#include <stdio.h>
#include <string.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int		i;

	i = 0;
	while (src[i] && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	unsigned int	i = 5;
	char src[] = "Il fait beau";
	char dest[] = "";

	ft_strncpy(dest, src, i);
	printf("%s\n", dest);
}

int	ft_is_prime(int nb)
{
	int	prime;

	prime = 2;
	if (nb < 2)
		return (0);
	while (prime <= nb / prime)
	{
		if (nb % prime == 0)
			return (0);
		prime++;
	}
	return (1);
}
